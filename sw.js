// When the service worker is installed, open a new cache and add all the files to it.
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        console.log('Opened cache and caching all app assets.');
        // addAll() is atomic - if one file fails, the whole operation fails.
        return cache.addAll(urlsToCache);
      })
      .catch(error => {
        console.error('Failed to cache one or more files:', error);
      })
  );
  self.skipWaiting();
});

// When the browser fetches a resource, try to serve it from the cache first.
// If it's not in the cache, fetch it from the network.
self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        // If the response is in the cache, return it.
        if (response) {
          return response;
        }
        // Otherwise, fetch the resource from the network.
        return fetch(event.request);
      })
  );
});

// This event will clean up old caches.
self.addEventListener('activate', event => {
  const cacheWhitelist = [CACHE_NAME];
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheWhitelist.indexOf(cacheName) === -1) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});